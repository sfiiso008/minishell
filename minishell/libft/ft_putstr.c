#include "libft.h"

void	ft_putstr(const char *s)
{
	int		i;
	char	*str;

	if (s)
	{
		i = 0;
		str = (char*)s;
		while (str[i] != '\0')
		{
			ft_putchar(str[i]);
			i++;
		}
	}
}
