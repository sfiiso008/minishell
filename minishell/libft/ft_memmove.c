#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	unsigned char	*d;
	unsigned char	*s;
	size_t			i;

	i = 0;
	d = (unsigned char*)dst;
	s = (unsigned char*)src;
	if (s < d)
	{
		while (len--)
			d[len] = s[len];
	}
	else
	{
		while (i < len)
		{
			if (d[i] != s[i])
				d[i] = s[i];
			i++;
		}
	}
	return (dst);
}
